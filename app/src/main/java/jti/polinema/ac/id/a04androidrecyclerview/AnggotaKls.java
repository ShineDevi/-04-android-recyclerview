package jti.polinema.ac.id.a04androidrecyclerview;

public class AnggotaKls {
    private String nama;
    private String absen;

    public AnggotaKls(String nama, String absen){
        this.nama = nama;
        this.absen = absen;
    }
    public String getNama() {
        return nama;
    }

    public void setName(String nama) {
        this.nama = nama;
    }

    public String getAbsen() {
        return absen;
    }

    public void setNim(String absen) {
        this.absen = absen;
    }
}
